		<p>Aaron Hillel Swartz (November 8, 1986 â€“ January 11, 2013) was an American computer programmer, 
writer, political organizer and Internet Hacktivist who committed suicide in the context of a prosecuti
on that was widely believed to be overly zealous and inappropriate. Swartz was involved in the development
 of the web feed format RSS and the Markdown publishing format,[4] the organization Creative Commons, 
 the website framework web.py and the social news site, Reddit, in which he became a partner after its 
 merger with his company, Infogami.
		</p>

		<p>
		Swartz's work also focused on sociology, civic awareness and activism. He helped launch 
		the Progressive Change Campaign Committee in 2009 to learn more about effective online activism. 
		In 2010 he became a research fellow at Harvard University's Safra Research Lab on Institutional 
		Corruption, directed by Lawrence Lessig. He founded the online group Demand Progress, known 
		for its campaign against the Stop Online Piracy Act.
		</p>

		<p>
		On January 6, 2011, Swartz was arrested by MIT police on state breaking-and-entering charges, 
		after systematically downloading academic journal articles from JSTOR. Federal prosecutors later 
		charged him with two counts of wire fraud and 11 violations of the Computer Fraud and Abuse Act, 
		carrying a cumulative maximum penalty of $1 million in fines, 35 years in prison, asset forfeiture, 
		restitution and supervised release.
		</p>

		<p>
		Swartz declined a plea bargain under which he would have served six months in federal prison. 
		Two days after the prosecution rejected a counter-offer by Swartz, he was found dead in his 
		Brooklyn, New York, apartment, where he had hanged himself.
		</p>
		
		<p>In June 2013, Swartz was posthumously inducted into the Internet Hall of Fame.</p>

