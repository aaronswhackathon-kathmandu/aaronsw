<p>Aaron Swartz Memorial Hackathon is an annual hackathon program tributed to Aaron Swartz 
(a.k.a. The Internet's Own Boy). He made lot of contributions to internet including RSS, 
Markdown, Reddit, Creative Commons licence, etc.<p>

<p>Every year on his birthday (November 8), many countries all over the world organize this
hackathon to develop the philosophy of Aaron Swartz about anonymity, and open access to
law and information.</p>

